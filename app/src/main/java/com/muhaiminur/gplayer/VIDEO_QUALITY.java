package com.muhaiminur.gplayer;

public class VIDEO_QUALITY {

    String zero;

    String one;

    String two;

    String three;

    String four;

    String five;

    public VIDEO_QUALITY() {
    }

    public VIDEO_QUALITY(String zero, String one, String two, String three, String four, String five) {
        this.zero = zero;
        this.one = one;
        this.two = two;
        this.three = three;
        this.four = four;
        this.five = five;
    }

    public String getZero() {
        return this.zero;
    }

    public void setZero(String value) {
        this.zero = value;
    }

    public String getOne() {
        return this.one;
    }

    public void setOne(String value) {
        this.one = value;
    }

    public String getTwo() {
        return this.two;
    }

    public void setTwo(String value) {
        this.two = value;
    }

    public String getThree() {
        return this.three;
    }

    public void setThree(String value) {
        this.three = value;
    }

    public String getFour() {
        return this.four;
    }

    public void setFour(String value) {
        this.four = value;
    }

    public String getFive() {
        return this.five;
    }

    public void setFive(String value) {
        this.five = value;
    }
}